var color = prompt ("Ingrese un color: ");
switch(color) {
    case "rojo":
    console.log("El color es rojo");
    break;

    case "azul":
    console.log("El color es azul");
    break;

    case "amarillo":
    console.log("El color es amarillo");
    break;

    default:
        console.log("Ingrese un color valido:");
        break;
}
